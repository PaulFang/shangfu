package com.xianpin365.entity;

public class CompanyProfile {

	private Integer id;

	private String language;

	private String neckBanner;

	private String profileTxt;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getLanguage() {
		return language;
	}

	public void setLanguage(String language) {
		this.language = language;
	}

	public String getNeckBanner() {
		return neckBanner;
	}

	public void setNeckBanner(String neckBanner) {
		this.neckBanner = neckBanner;
	}

	public String getProfileTxt() {
		return profileTxt;
	}

	public void setProfileTxt(String profileTxt) {
		this.profileTxt = profileTxt;
	}

}
